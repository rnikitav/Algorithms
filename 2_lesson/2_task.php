<?php
$n = 10000;
$array[]= [];

for ($i = 0; $i < $n; $i++) {  // O(n)
    for ($j = 1; $j < $n; $j *= 2) { //  O(log n)
        $array[$i][$j]= true;
    }
}
// Первый O(n) Второй O(log n) => O( n log n)



